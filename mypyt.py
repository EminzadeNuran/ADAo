# Dəyişənlər və Məlumat Tipləri (int, float, str, bool, list, tuple, dict, set)
a = 10
b = 3

# Riyazi Əməliyyatçılar
print(a + b)  # 13
print(a ** b) # 1000

# Müqayisə Əməliyyatçılar
print(a > b)  # True
print(a == b) # False

# Məntiqi Əməliyyatçılar
x = True
y = False
print(x and y) # False

# Təyin Əməliyyatçılar
a += 5
print(a) # 15

# Bit Əməliyyatçılar
print(a & b) # 2

# Üzvlük Əməliyyatçılar
print("a" in "apple") # True

# Şəxsiyyət Əməliyyatçılar
print(a is b) # False

# Sətir Əməliyyatçılar
print("a" * 3) # aaa

# List Əməliyyatçılar
my_list = [1, 2, 3]
print(my_list[0]) # 1
print(my_list + [4, 5, 6]) # [1, 2, 3, 4, 5, 6]

# Tuple Əməliyyatçılar
my_tuple = (1, 2, 3)
print(my_tuple[0]) # 1